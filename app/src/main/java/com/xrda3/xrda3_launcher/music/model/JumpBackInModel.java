package com.xrda3.xrda3_launcher.music.model;


public class JumpBackInModel {

    String title;
    int imagesRes;

    public JumpBackInModel(String title, int imagesRes) {
        this.title = title;
        this.imagesRes=imagesRes;
    }

    public String getTitle() {
        return title;
    }
    public int getImageRes() {   // capital I
        return imagesRes;
    }

}


